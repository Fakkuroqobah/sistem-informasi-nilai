    private void alertWarning(String text) {
        JOptionPane.showMessageDialog(this, text, "Validasi", JOptionPane.WARNING_MESSAGE);
    }
    
    private boolean validasiForm() {
        if(txtkd_matkul.getText().isEmpty()) {
            alertWarning("Kode Matkul tidak boleh kosong");
            return true;
        }else if(txtkd_matkul.getText().length() > 5) {
            alertWarning("Panjang Kode Matkul tidak boleh lebih dari 5 karakter");
            return true;
        }
        
        if(txtnama_matkul.getText().isEmpty()) {
            alertWarning("Nama Matkul tidak boleh kosong");
            return true;
        }else if(txtnama_matkul.getText().length() > 20) {
            alertWarning("Panjang Nama Matkul tidak boleh lebih dari 20 karakter");
            return true;
        }
        
        return false;
    }

    private void tampilkan_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Kode");
        model.addColumn("Nama Matkul");
        
        try{
            int no = 1;
            ResultSet res = Db.runQuery("SELECT * FROM matkul");
            
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2)});
            }
            tabelmatkul.setModel(model);
            
        }catch (SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
    }

