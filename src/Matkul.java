    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        if(!validasiForm()) {
            try{
                Db.runUpdate("CALL proc_insert_matkul("
                    + "'"+txtkd_matkul.getText()+"',"
                    + "'"+txtnama_matkul.getText()+"')");

                JOptionPane.showMessageDialog(null,"Proses Simpan Data Berhasil....");
                tampilkan_data();
                kosongkan_form();
            }catch(HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_simpanActionPerformed

    private void tabelmatkulMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelmatkulMouseClicked
        int baris = tabelmatkul.rowAtPoint(evt.getPoint());
        
        String kode = tabelmatkul.getValueAt(baris, 1).toString();
        txtkd_matkul.setText(kode);
        txtkd_matkul.setEnabled(false);
        
        String nama = tabelmatkul.getValueAt(baris, 2).toString();
        txtnama_matkul.setText(nama);
        
        simpan.setEnabled(false);
        tbubah.setEnabled(true);
        tbHapus.setEnabled(true);
    }//GEN-LAST:event_tabelmatkulMouseClicked

    private void tbubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbubahActionPerformed
        if(!validasiForm()) {
            try{
                Db.runUpdate("UPDATE matkul SET "
                    + "nama_matkul = '"+ txtnama_matkul.getText() +"'"
                    + "WHERE kd_matkul = '"+ txtkd_matkul.getText() +"'");

                JOptionPane.showMessageDialog(null,"Ubah Data Berhasil...");
            }catch(HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }

            tampilkan_data();
            kosongkan_form();
            txtkd_matkul.setEnabled(true);
        }
    }//GEN-LAST:event_tbubahActionPerformed

    private void tbHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbHapusActionPerformed
        try{
            Db.runUpdate("DELETE FROM matkul WHERE kd_matkul='"+ txtkd_matkul.getText() +"'");
            
            JOptionPane.showMessageDialog(null,"Hapus Data Berhasil...");
        }catch(HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        tampilkan_data();
        kosongkan_form();
        txtkd_matkul.setEnabled(true);
    }//GEN-LAST:event_tbHapusActionPerformed

